package com.weebly.nsgamezz.tictactoe;

public class BluetoothAccessException extends Exception {

    public BluetoothAccessException() {
        super();
    }

    public BluetoothAccessException(String message) {
        super(message);
    }

    public BluetoothAccessException(String message, Throwable cause) {
        super(message, cause);
    }

    public BluetoothAccessException(Throwable cause) {
        super(cause);
    }
}
